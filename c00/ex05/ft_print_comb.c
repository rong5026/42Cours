#include <unistd.h>

void	ft_print_comb(void)
{
	char	str[3];

	str[0] = '0' - 1;
	while (str[0]++ < '7')
	{
		str[1] = str[0];
		while (str[1]++ < '8')
		{
			str[2] = str[1];
			while (str[2]++ < '9')
			{
				if (str[2] > str[1] && str[1] > str[0])
				{
					write(1, &str, 3);
					if (str[0] != '7')
					{
						write(1, ", ", 2);
					}
				}
			}
		}
	}
}
