char	*ft_strupcase(char *str)
{
	char	*result;

	result = str;
	while (*str)
	{
		if (('a' <= *str && *str <= 'z'))
		{
			*str = *str - 'a' + 'A';
		}
		str++;
	}
	return (result);
}
